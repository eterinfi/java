package data.type.numbers;

public class NumberDemo {
	public static void main(String[] args) {
		int a = 5000;
		float b = 13.65f;
		byte c = 0x4a;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println();
		Integer x = 5;
		x = x + 10;
		System.out.println(x);
	}
}